#include<iostream>
using namespace std;
int main()
{
    int num1 , num2;
    // << is called INSERTION operator //
    cout<<"Enter first number: ";
    // >> is EXTRACTION operator // 
    cin>> num1;
    
    cout<<"Enter second number: ";
    cin>> num2;

    cout << "The sum of two numbers is "<<num1+num2;
    return 0;
}
